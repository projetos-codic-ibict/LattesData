function bs_pages($ini, $stop, $link = '')
{
    $sx = '';
    $sx .= '<nav aria-label="Page navigation example">' . cr();
    $sx .= '<ul class="pagination">' . cr();
    for ($r = $ini; $r <= $stop; $r++) {
        $xlink = base_url($link . '/' . chr($r));
        $sx .= '<li class="page-item"><a class="page-link" href="' . $xlink . '">' . chr($r) . '</a></li>' . cr();
    }
    $sx .= '</ul>';
    $sx .= '</nav>';
    return ($sx);
}
