function breadcrumbs($its = array())
{
    if (count($its) == 0) {
        $path = geturl();
        $p = explode('/', $path);
        $dir = '';
        for ($r = 3; $r < count($p); $r++) {
            if ($dir != '') {
                $dir .= '/';
            }
            $dir .= $p[$r];
            $name = lang(troca($dir, '/', '.'));
            $its[$name] = PATH . MODULE . $dir;
        }
    }
    $sx = '';
    $sx .= '<nav aria-label="breadcrumb">' . cr();
    $sx .= '<ol class="breadcrumb">' . cr();
    foreach ($its as $label => $link) {
        $linkl = '<a href="' . $link . '">';
        $linka = '</a>';
        if (strlen($link) == 0) {
            $linka = '';
            $linkl = '';
        }
        $sx .= '<li class="breadcrumb-item active" aria-current="page">' . $linkl . lang($label) . $linka . '</li>';
    }
    $sx .= ' </ol></nav>';
    $sx = bs(bsc($sx, 12));
    return $sx;
}
